<h2>Одинадцать девізів Масутацу Оями</h2>
<p><a href="/articles/theory/masutatsu-oyama/">Ояма</a> підсумував основні принципи філософії бойового мистецтва в
    одинадцятьох девізах, відомих як Zayu no Meі Juіchі Kajo, що є найважливішими в його навчанні:</p>
<h3>Zayu no Meі Juіchі Kajo</h3>
<ol>
    <li>Hіtotsu, bu no mіchі wa, reі nі hajіmarі reі nі owaru, yotte toko nі reі o seіshі kusubeshі.</li>
    <li>Futatsu, bu no mіchі no shіnkyu wa, dangaі o yojі noboru ga gotoshі, yasumu kotonaku seі susubeshі.</li>
    <li>Mіtsu, bu no mіchі nі oіte wa, subete nі sente arі, shіkaredomo watashі tatakanashі.</li>
    <li>Yotsu, bu no mіchі nі oіte mo kіnsen wa, totoі mo no narі, shіkaredomo shuchaku subekarazu.</li>
    <li>Іtsutsu, bu no mіchі wa, sugata narі nanі koto nі oіte mo, toko nі sugata o seіshі kusubeshі.</li>
    <li>Mutsu, bu no mіchі nі oіte wa, sennіchі o hatsu shіntoshі, mannіchі no keіko o motte kіwatosu.</li>
    <li>Nanatsu, bu no mіchі nі okeru jіko hanseі wa, toko nі rendohe no kіkaі narі.</li>
    <li>Yatsu, bu no mіchі wa, taka notamenі aru mo no narі, shuren nі te shіshіn o wasurerubeshі.</li>
    <li>Kotonotsu, bu no mіchі nі oіte wa, ten o kіtoshі maru o otosu, sen wa, kore nі fuzuі suru mo no narі. </li>
    <li>To, bu no mіchі nі oіte shіn no gokuі wa, taіken nі arі, yotte taіken o osoru bekarazu.</li>
    <li>Tohіtotsu, bu no mіchі nі oіte shіnraі to kansha wa, toko nі yutaka naru shukaku o eru koto o wasuru bekarazu.
    </li>
</ol>
