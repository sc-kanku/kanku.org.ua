            <li>шин (яп. 真, правда, дійсність, реальність, істина);</li>
            <li>кай (яп. 会, організація, суспільство, союз).</li>
        </ul>

        <p>Найбільш часто зустрічається в літературі трактування символу «Кіокушинкай» -
            союз абсолютної істини, хоча нашому клубу ближчим є переклад «пошук істини на шляху бойового
