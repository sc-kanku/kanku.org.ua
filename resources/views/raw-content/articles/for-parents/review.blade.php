<h3><a href="https://www.facebook.com/profile.php?id=100001396888529">Тетяна</a>, мама першокласниці-спортсменки</h3>
<p>
    <a title="Аккаунт автора відгуку в FB" href="https://www.facebook.com/profile.php?id=100001396888529">
        <img class="rounded ilustration" src="/images/vidguki/max.jpg" alt="Відгуки батьків юних каратистів" />
