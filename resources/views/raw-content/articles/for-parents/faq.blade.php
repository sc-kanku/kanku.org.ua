<p class="question">Наскільки високим є рівень навчання в вашому клубі?</p>

<p>Найчесніший та наочний показник рівня навчання — це перемоги на змаганнях. І тут нам є чим пишатися: наші спортсмени
    постійно сходять на п'єдестали першостей України та Європи — читайте нашу <a href="/blog/">стрічку новин</a>.</p>

<p class="question">Коли син матиме можливість брати участь у змаганнях? </p>

<p>Це дуже хороше запитання, яке вказує на один із складних пунктів у вихованні спортсмена-початківця: тренування ще
    незвично важкі, а нагороди у вигляді змагань та визнання ще так далекі, і немає шансів на успіхи в чемпіонатах
    України, бо їх рівень дуже й дуже високий. Поспішаємо запевнити, що ми цю проблему усвідомили, й для її вирішення
    регулярно проводимо внутрішньоклубні змагання «Перший крок». Це змагання спортсменів-початківців, на яких вони
    отримають свої перші нагороди, й радітимуть своїм першим перемогам. </p>

<p>Дайте своїм дітям відчуття радості перемог!</p>

