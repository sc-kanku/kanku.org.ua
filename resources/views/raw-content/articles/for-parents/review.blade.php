<h1>Відгуки батьків юних спортсменів клубу «Канку»</h1>

<h3>Максимчук Світлана, мама Юліана (5 років)</h3>
<p>Карате додало в наше життя нових барв. Прийшовши випадково і тимчасово (ми шукали гурток на пів року, з будь-якою
    фізичною активністю, щоб син на дивані цілодобово не сидів з книжками) ми закохались і залишились. І це надовго. На
    моїх очах з маленького маминого синочка вигартовується чоловік. Мій маленький солодкий пупсичок демонструє таку волю
    і силу характеру, що моєму жіночому мамському началу просто не по собі. Дуже приємно вражена нашим тренером. Попри
    свою молодість він показав себе абсолютно відповідальним і зрілим чоловіком. І звичайно, як мама, я дуже рада, що в
    Юліановому оточенні з'явився такий достойний приклад для наслідування. Не зважаючи на те, що заняття проводяться з
    групою дітей, мене не покидає відчуття індивідуальності процесу. За моїм малюком слідкують, в нього вникають,
    цікавляцься, в нього вкладають. Кожне тренування це як мінімум кілька ситуацій для опрацювання, аналізу і виховання.
    Це і сиуації пов'язані з усвідомленням себе і з відносинами сина з іншими, і якісь внутрішні його особистісні
    якості, це те, як він керує тою силою, яка в ньому зростає і море всього іншого. Це просто відмінна школа виховання
    і неоціненно велика допомога батькам у зрощуванні Людини (так, з великої літери).</p>
<p>Я не могла залишитись осторонь і в якийсь момент також долучилась до прекрасного. Для мене, вже дорослої, карате це в
    першу чергу ще одна частинка пазлу до розуміння себе, хто я, чому і як я проживаю труднощі, чому ті чи інші речі
    дарують радість, що мене мотивує і чому, це і робота з болем, виявлення і аналіз якихось внутрішніх блоків, це все
    ключики на шляху до себе. Переді мною багато ще невідомого, незрозумілого, такого дивного, величного і прекрасного.
    Дякую своїм мудрим наставникам, що так грамотно і тонко взялись зі мною працювати в такий неоднозначний для мене
    час. Дякую за вашу щирість та відданність справі, це дуже заражає та надихає.</p>
<p>05 травня 2016 р. Автор: Світлана Максимчук</p>

<h3><a href="https://www.facebook.com/oksana.bilenko.712">Оксана</a>, спортсменка, мама двох юних спортсменів СК КАНКУ
</h3>
<p>
    <a title="Аккаунт автора відгуку в FB" href="https://www.facebook.com/oksana.bilenko.712">
        <img class="rounded ilustration" src="/images/users/image36.jpg" alt="Оксана Біленко мило усміхається" />
