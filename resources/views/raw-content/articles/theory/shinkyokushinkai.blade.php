<h1>ШинКіокушинкай</h1>

<p>У 1994 році Сосай <a href="../masutatsu-oyama/">Масутацу Ояма</a> покинув нас. І сталося те, що часто відбувається в
    світі людей: учні побилися за спадок Вчителя. І ця сварка — не найкраща сторінка історії кіокушин-карате. Ми не
    будемо її тут переказувати: про неї краще скоріше забути.</p>

<p>Але доведеться визнати, що канчо Ш.Мацуї, що формально став спадкоємцем Оями, не став, і, мабуть, не міг стати тією
    фігурою, яка об'єднала б учнів Оями. Може, тому ІКО стала вдаватися до крутійства: реєструвати назву кіокушинкай і
    його символіку як торгову марку. Це обмеження стосувалося всесвітньо відомих символів:</p>

<h3>Ієрогліфічний символ Кіокушинкай</h3>

