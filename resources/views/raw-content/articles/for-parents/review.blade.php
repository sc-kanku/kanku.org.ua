    Моєму сину Максиму 11 років, а почав займатись карате з 6 років. За ці роки я бачу лише плюси для нього і не тільки
    для нього (а про це згодом розкажу). Максим росте здоровим, міцним хлопцем, ходить зараз у 5 клас, і за цей час ні
    разу не пропустив школу із-за хвороби!
</p>
<p>Подобається, як син вирішує конфлікти – не лізе битись, а веде діалог, розбирається в причині конфлікту. Ще в
    дитячому садочку його називали «дипломат», бо вмів вирішити конфлікт без сили, словами. Цього теж вчать на карате –
    стримуватись від насильства. Одного разу довелось йому зіткнутись з ситуацією, коли один хлопчик не зрозумів слів і
    таки ліз битись… Максим його не бив, а схопив так, що той не зміг поворухнутись, і пояснив, що не варто зачіпати
    його та його друзів. Минуло вже більш ніж 2 роки з того часу, цей хлопчик більше не конфліктує з іншими дітьми у
    дворі, так як зрозумів, що не все вирішується силою, і головне – що на його силу завжди може знайтись ще більша
    сила, отже, конфлікти треба намагатись вирішувати словами.</p>
<p>Також моя старша донька прийшла в карате у підлітковому віці, в 15 років. Насправді, нема вікових обмежень! Я
    переконалась в цьому особисто. З дитинства я мріяла займатись карате, але на жаль, не мала такої можливості… Коли я
    подорослішала і стала самостійною, то думала, що вже пізно починати, і тільки по доброму заздрила своїм дітям…поки
    одного разу не прийшла на тренування. Мені було 36 років. І тепер, коли отримала свій перший пояс (помаранчевий, 10
    кю), я розумію, що нема вікових обмежень! Скажу одне - ми часто недооцінюємо свої можливості, і можемо насправді
    набагато більше, ніж здається (перевірено на собі). Головне - повірити в себе і перебороти себе, свій страх,
    невпевненість, своє "не хочу" чи "не можу". Не боятись помилок, а боятись стояти на місці.</p>
<p>Залишаючи цей відгук, хочу скористатись можливістю і запросити всіх познайомитись з кіокушинкай карате! Всіх, хто
    сильний духом або хоче таким стати! Всіх, хто хоче вдосконалювати свої тіло, характер, витримку! Всіх, хто хоче
    знайти приємне спілкування і з користю проводити час.</p>
