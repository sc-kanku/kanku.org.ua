<h3>Канку</h3>

<figure class="row mt-4">
    <div class="col-2 text-center">
        <img class="rounded" src="/images/articles/kanky.gif" alt="Символ Канку" />
    </div>

    <figcaption class="col">Символ Канку походить з ката Канку-дай. У цій ката руки піднімаються по колу,
        ніби окреслюючи небо, і так формується символ. Точки в Канку представляють пальці і позначають межі,
        або піки. Товсті секції представляють зап'ястя і позначають силу. Центр представляє нескінченність,
        позначаючи глибину. Символ, вписаний в коло і окреслений ним, означає плинність і круговий рух.
    </figcaption>
</figure>

