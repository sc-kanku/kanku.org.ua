<h2>Удосконалення техніки</h2>

<p>Техніки карате вимагають доброї злагодженості рухів рук і ніг. Разом з тим вони вимагають гарної «керованості»: руки
    і ноги повинні виконувати команди, які ваша дитина їм подає. Далеко не всім це дано від природи, але більшість дітей
    справляються з техніками на тренуваннях. Але буває, що тренувань мало для хорошої успішності в засвоєнні техніки. Не
    страшно, багато спортсменів через це пройшли — і виправили координацію рухів. Це стане в нагоді не тільки в карате.
</p>

<p>Нехай Ваша дитина відпрацьовує техніки перед дзеркалом — спочатку повільно, а потім прискорюючись. Найкращий тренер —
    це дзеркало. Звісно, для кращого контролю на початковому рівні Ви можете сміливо запитувати у Вашого тренера, над
    чим варто попрацювати, при потребі попросіть продемонструвати та пояснити інстуктора.</p>

<p>А якщо дитина не встигає до атестації? Нічого страшного. Атестація буде пройдена в наступний раз. А в цей раз юний
    спортсмен відвідає семінари, які в нашому клубі обов'язково проходять перед іспитом - вони дуже допоможуть в
    освоєнні техніки.</p>

<p>Вашому синові або доньці, звичайно ж, буде прикро встати в строю після тих, хто здали атестацію в цей раз. Подолання
    цієї першої перешкоди - прекрасна школа життя. Завжди хтось буде обганяти, і треба вчитися не переживати через це, а
    радіти своєму прогресу, ставити нові цілі.
