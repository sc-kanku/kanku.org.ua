    Моя донька займається
    кіокушинкай-карате з п’ятирічного віку. Діані скоро виповниться сім. Хоч вона ще не брала участі у змаганнях, а
    значить, не має перемог та відзнак, є багато позитивних моментів у цьому навчанні як для неї, так і для мене. Хочу
    їх коротко перелічити.
</p>
<p>Витривалість та фізична форма. На тренуваннях діти, крім відпрацювання техніки, багато бігають, віджимаються,
    бавляться у силові ігри, качають прес. Відповідно, дитина звикла до значних фізичних навантажень та любить їх (от у
    цьому вбачаю заслугу нашого тренера <a href="/coaches/zhukov-ruslan/">Руслана Вікторовича Жукова</a>). Цього року
    Діана пішла у перший клас. Цей перехід від безтурботного життя до шкільних навантажень, дякуючи спорту, дався їй
    доволі легко. Дитина без проблем носить важкий рюкзак, бадьора на уроках та весела на перервах, легко прокидається
    вранці, вкрай рідко виявляє ознаки втоми. Хороша фізична форма, як мені здається, дає запас витривалості, такий
    необхідний для учениці першого класу.</p>
<p>Соціальні навички взаємодії в колективі. Донька схильна до лідерства й хоче уваги від однокласників та однокласниць.
    Тут, як я бачу, у пригоді Діані стають її вміння швидко бігати та високо стрибати. На шкільному подвір’ї вона
    залазить по драбині вище за всіх – адже це стандартна вправа на розвиток сили та координації рухів, яку вона
    регулярно виконує на тренуваннях. Постійний дух змагання, організація ігр та вміння брати участь у них – необхідні
    елементи спортивних тренувань, які неодмінно стануть доньці у пригоді в колективі. А якщо доведеться програвати… на
    спорті теж навчать програвати гідно та без зайвих емоцій. Це теж може дуже придатися в житті. Так само як і вміння
    себе захистити, чому карате неодмінно її навчить.</p>
<p>Нові друзі та подорожі. Донька з задоволенням йде на тренування ще й через те, що рада зустрітися там зі своїми
    друзями. Після тренування вони обмінюються новинами, іноді навіть подарунками. Бачу щире, радісне та безпосереднє
    спілкування дітей, і сама не можу від цього не радіти. Спільні ігри, спільне подолання навантажень на тренуваннях
    зближують та сприяють справжній дитячій дружбі. А ще їй дуже сприяють походи секції на природу. Діти під наглядом
    тренера ночують у палатках, готують їжу на багатті, тренуються на природі – переживають разом чимало справжніх
    чудових пригод. Діана досі згадує такий літній похід. Коли вона підросте, ще й поїде на тиждень із секцією в
    Карпати! Гадаю, їй це також дуже сподобається.</p>
<p>Я вдячна нашому тренеру й СК «Канку» загалом: нам пощастило обрати дуже добрий шлях для розвитку доньки. Щодня з
    радістю бачу поруч щасливу, здорову та активну дитину. У цьому неабияка заслуга кіокушин-карате. Бажаю всім батькам
    також знайти свій шлях та найкращого тренера для своєї дитини.</p>
