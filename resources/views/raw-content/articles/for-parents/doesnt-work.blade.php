<h2>Вдосконалення фізичної підготовки</h2>

<p>У будні дні, коли немає тренування, слід додатково займатися вечорами. Виконати три підходи по три вправи, які
    потрібні для атестації (і які відпрацьовуються на тренуваннях): віджимання на кулаках, підняття тулуба (вправи для
    пресу) і присідання. Разом дев'ять підходів.</p>

<p>Частоту повторень в підході Ви з юним спортсменом визначте самі: три останніх повторення потрібно зробити через
    велике «не можу». Саме ці повторення найбільш цінні для зростання фізичних показників. Послухайте, що говорить про
    це легендарний Арні:</p>

